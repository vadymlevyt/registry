/**
 * Зібрати промпт Triage.
 * @param {object} args
 * @param {Array<{fileId,name,origin?,pageCount?,passport:string}>} args.artifacts
 *        — артефакти після нормалізації; passport = текст зі структурними
 *        маркерами "=== СТОРІНКА N ===" (Ф0).
 * @param {string} [args.userHint] — контекст від адвоката / court_sync
 * @returns {string}
 */
export function buildTriagePrompt({ artifacts = [], userHint = '' } = {}) {
  const head = `Це набір файлів судової справи (фото, скани, PDF, фрагменти — разом).${userHint ? ` Контекст: ${userHint}` : ''}

Подивись на ВЕСЬ набір і визнач, що з кожним зробити. Поверни ЄДИНИЙ план:
які логічні документи присутні, з яких файлів/сторінок кожен складається,
і який маршрут (route) для кожного.

${ROUTE_DOC}

ЩО ТИ БАЧИШ НА ВХОДІ:

Кожна сторінка приходить як два шари.

Перший шар — текст: OCR-розпізнаний "_text" сторінки. Може бути неповний —
після маркера "=== СТОРІНКА N ===" іноді показано лише краї тексту: перші
і останні рядки, а між ними "⟨…⟩" означає вирізане тіло (так стискається
великий том, щоб справа вмістилась у вікно моделі). На малих томах текст
подано повністю.

Другий шар — метадані Document AI: рядок у дужках "[...]" після маркера
сторінки. Це стиснуті в короткі теги результати аналізу скана від Google
Document AI:
- геометрія: "орієнтація:0°", "формат:портрет(0.71)" (типовий А4),
  "формат:квадрат" (типове фото).
- структура: "таблиці", "поля-форми", "таблиця-домінює:XX%".
- заголовки і футери: "заголовок:..." (короткий центрований блок зверху
  сторінки), "футер-№:N" (надрукований номер сторінки внизу).
- внутрішня нумерація документа: "док-стор:N/M", теги "ПОЧАТОК-ДОКУМЕНТА"
  і "КІНЕЦЬ-ДОКУМЕНТА".
- підписи і печатки: "печатка/підпис:signature", "печатка/підпис:stamp".
- щільність контенту: "розріджена" (мало тексту і мало блоків на сторінці).
- якість скана: "стрибок-якості:Δ0.XX", "дефекти-зміна", "OCR-низька:0.XX".
- зміни умов: "зміна-формату", "зміна-орієнтації", "зміна-мови:uk→en",
  "СКИДАННЯ-НУМЕРАЦІЇ".

Метадані — це сирий матеріал для твого аналізу, не готовий вердикт.
"розріджена" сама по собі не каже "це кінець" — вона каже "на цій сторінці
мало контенту". Чи це кінець документа, чи обкладинка, чи квитанція —
вирішуєш ти, склавши цей факт з іншими тегами, з текстом сторінки і з
тим що було на сусідніх сторінках.

ЯК ДУМАТИ ПРО МЕЖІ ДОКУМЕНТІВ:

Уяви себе адвокатом-сортувальником: на столі набір сторінок з тому судової
справи. Як правило сторінки одного документа лежать поруч і в правильному
порядку — це звичайна ситуація, з якої корисно починати. Але це лише
орієнтир, не закон: трапляються фото зроблені в різні спроби, документ
розрізаний на кілька файлів, переплутаний порядок аркушів, кілька
документів змішані в одному скані. Перевіряй цю гіпотезу щоразу
самостійно — за текстом, за метаданими, за маршрутами (image_merge і
fragment_reconstruct прямо передбачають що сторінки одного документа
можуть бути розкидані).

Твоя задача — побачити де один документ закінчується, а наступний
починається, навіть якщо для цього треба зібрати сторінки з різних файлів
або в нестандартному порядку.

Аналізуй усі сигнали в сукупності — текст плюс метадані Document AI плюс
контекст інших сторінок — і на основі цього аналізу приймай рішення про
межі. Жоден сигнал сам не вирішує. Якщо на сторінці немає ні ознак
початку нового документа, ні ознак кінця попереднього — скоріш за все
вона належить попередньому документу.

ОРІЄНТИРИ ПОЧАТКУ ДОКУМЕНТА (висока ймовірність першої сторінки):
- "ЯКІР-ДОКУМЕНТА" — заголовок зверху сторінки містить типове слово
  українського юр. документа (ПОСТАНОВА, УХВАЛА, РІШЕННЯ, ВИРОК, ВИСНОВОК,
  ПРОТОКОЛ, АКТ, ЗАЯВА, ПОЗОВНА, ДОВІДКА, СВІДОЦТВО, ДЕКЛАРАЦІЯ, ДОГОВІР,
  ОРДЕР, КЛОПОТАННЯ, СКАРГА, ВИМОГА, ПОВІДОМЛЕННЯ, ВИТЯГ, ЛИСТ і подібні).
  Перелік невичерпний — якщо бачиш інший очевидний заголовок-назву,
  кваліфікуй його теж як новий документ.
- "заголовок:..." — короткий центрований блок зверху сторінки (шапка,
  фірмовий бланк суду, адресат, реквізити). Окремо — "можливо", разом із
  ЯКОРЕМ — "майже точно".
- "ПОЧАТОК-ДОКУМЕНТА" — внутрішня нумерація документа «1 з N» / «1/N» /
  «Page 1 of N» / «-1-». Майже завжди = перша сторінка свого документа.
- "СКИДАННЯ-НУМЕРАЦІЇ" — футер тома впав назад (попередня 18, ця 1). Ця
  сторінка починає новий документ.

Будь-який з цих сигналів сам — підстава розглядати початок документа.
Поєднання двох-трьох — практично впевнено початок.

ОРІЄНТИРИ КІНЦЯ ДОКУМЕНТА (висока ймовірність останньої сторінки):
- "КІНЕЦЬ-ДОКУМЕНТА" — внутрішня нумерація «N з N». Документ завершується
  на цій сторінці.
- "печатка/підпис:signature" + "розріджена" разом — підпис під текстом
  який не заповнив сторінку (класична остання сторінка українського юр.
  документа: тіло закінчилось, підписант розписався, лишилось місце).
  Сильний сигнал кінця.
- "печатка/підпис:stamp" поряд із підписом — додатково підсилює (печатка
  під/біля підпису = офіційне завершення).

Зауваж: підпис і печатка можуть стояти й НЕ на останній сторінці
(нумеровані сторінки експертизи з печаткою на кожній; парафа на кожній
сторінці договору). Тому сам підпис або сама печатка — недостатньо.
Потрібна КОМБІНАЦІЯ з ознакою "текст не заповнив сторінку" (тег
"розріджена") або з внутрішньою нумерацією N/N.

ДОПОМІЖНІ СИГНАЛИ (зважуй разом, жоден сам не вирішує):
- "стрибок-якості:Δ0.XX" — різка зміна якості скана vs попередня (нова
  сесія сканування).
- "дефекти-зміна" — змінився набір detectedDefects vs попередня (інша
  камера, освітлення, умови сканування). Сильніше за самотній qualityScore.
- "розріджена" — мало тексту, мало блоків (обкладинка, розділювач, титулка
  нового документа).
- "зміна-формату" / "зміна-орієнтації" — інший dimension/orientation vs
  попередня сторінка.
- "зміна-мови:uk→en" — переключення detectedLanguages[0].
- "док-стор:N/M" — внутрішня нумерація з загальною довжиною документа.
  Підказує реальну довжину поточного документа (де очікувати кінець).
- "таблиця-домінює:XX%" — велика таблиця займає >40% сторінки. Часто
  сторінка-реєстр / зміст / опис.

МЕТАІНФОРМАЦІЯ (НЕ сигнал межі — попередження):
- "OCR-низька:0.XX" — середня confidence paragraphs <0.7 на сторінці. Не
  довіряй тонким сигналам на цій сторінці; не створюй короткий "документ"
  лише через слабкі підказки на ній — це може бути шум OCR.

ТИПОВИЙ ВИПАДОК — БЕЗ СИГНАЛІВ МЕЖІ:

Якщо на стику двох сусідніх сторінок ані попередня не показує ознак
кінця, ані наступна — ознак початку, скоріш за все це продовження того
самого документа (звичайна більшість сторінок тома). Не вигадуй межу
там де її не видно. Але звір ще раз із текстом: якщо зміст явно стрибає
(інший суд, інший заявник, інша справа, інший період) — це межа без
формальних маркерів, постав її.

Українські судові патерни (важливо для точних меж):
- Квитанція / платіжна інструкція про сплату судового збору — ЗАВЖДИ
  ОКРЕМИЙ документ (власний діапазон сторінок), НІКОЛИ не приклеюй її до
  позовної заяви чи до титульної/обкладинкової сторінки, навіть якщо вони
  поруч або на одному скані. Зазвичай 1 сторінка, свій заголовок.
- Обкладинка справи / супровідний лист канцелярії / штамп суду —
  route to_fragments (службове, не самостійний документ).
- Коротка сторінка (1 стор.) з власним заголовком, відмінним від сусідів
  (квитанція, довідка, конверт, опис вкладення) — окремий документ, а не
  частина сусіднього.
- Короткі документи в кримінальних та адміністративних справах (вимоги
  1-2 стор., постанови 1-3, протоколи 2-5, довідки 1) — звичайна норма.
  НЕ зливай їх в один документ тільки тому що сусідні.

ПРИНЦИП РІШЕННЯ:

Думай, а не намагайся буквально виконати інструкцію. Інструкції тут —
орієнтир і допомога, а не догма. Списки сигналів, порогові значення,
"висока ймовірність" / "майже точно", припущення про послідовність
сторінок, типові патерни — це підказки досвіду адвоката, які допомагають
швидше зорієнтуватись. У реальному наборі може бути нетипова шапка,
відсутній підпис, дивний скан, гібридний документ, фото в довільному
порядку, документ розрізаний по файлах. Усе перевіряй сам. Якщо текст і
метадані разом кажуть тобі що це межа — постав її, навіть якщо
формального ЯКОРЯ немає. Якщо орієнтири підказують межу, а зміст явно
тягнеться далі — не став її. Якщо сторінки одного документа розкидані —
збери їх через image_merge або fragment_reconstruct. Ти бачиш сторінки
цілісно, інструкції — лише дороговказ.

startPage/endPage визначай ВИКЛЮЧНО за маркерами "=== СТОРІНКА N ===".
Послідовні імена файлів-фото (IMG_001..) або спільний формат-фото — сигнал
image_merge. Обрив тексту в кінці одного файла і продовження на початку
іншого — сигнал fragment_reconstruct.

Поверни ТІЛЬКИ JSON без тексту до/після:
{
  "documents": [
    {"documentId":"d1","name":"Позовна заява","type":"pleading","route":"slice","fragments":[{"fileId":"f0","startPage":1,"endPage":8}],"open":false}
  ],
  "unusedPages": [{"fileId":"f0","startPage":9,"endPage":9,"reason":"порожня сторінка"}]
}

Типи (type): pleading, court_act, evidence, certificate, contract,
court_cover, correspondence, other.
ВАЖЛИВО: межі і маршрути — лише за реальним вмістом. Не вигадуй документи.`;

  const lines = [head, '', `Файлів у наборі: ${artifacts.length}`, '', 'ФАЙЛИ:'];
  for (const a of artifacts) {
    lines.push('---');
    lines.push(`fileId: ${a.fileId}`);
    if (a.name) lines.push(`name: ${a.name}`);
    if (a.origin) lines.push(`origin: ${a.origin}`);
    if (Number.isFinite(a.pageCount)) lines.push(`pageCount: ${a.pageCount}`);
    lines.push('passport:');
    lines.push(a.passport || '(порожній — файл без розпізнаного тексту/структури)');
  }
  return lines.join('\n');
}
