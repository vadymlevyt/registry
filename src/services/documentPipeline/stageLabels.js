// ── DOCUMENT PIPELINE · STAGE LABELS (G0 — діагностика/UX) ──────────────────
// Чистий примітив: технічна назва стадії диригента → людський підпис для
// адвоката у прогрес-екрані. Корінь bug 7: UI показував буквальне
// "Стадія: processing" увесь час (reportProgress хардкодив 'processing',
// диригент назву стадії не повідомляв). Тут — ЛИШЕ мапа+геттер; інжекцію
// onStage у диригент і push у jobProgressStore робить streamingExecutor.
//
// Один сенс на запис (правило #11): підпис описує ЩО зараз відбувається
// мовою адвоката, не внутрішню механіку. Без емодзі (§2.9). 'ocr' — псевдо-
// стадія: потоковий chunk-OCR виконує streamingExecutor ДО диригента
// (extract диригента — постобробка, не сам OCR), адвокату це теж «стадія».

// STAGE-значення дзеркалять documentPipeline.js STAGE (НЕ імпортуємо щоб
// модуль лишався чистим без циклу; тест звіряє повноту покриття).
export const STAGE_LABELS = Object.freeze({
  ocr: 'Розпізнавання тексту',
  intake: 'Підготовка файлів',
  detectBoundaries: 'Аналіз структури документів',
  extract: 'Підготовка тексту',
  confirm: 'Підтвердження плану',
  persist: 'Розкладання документів',
  emit: 'Завершення',
});

/**
 * Людський підпис стадії. Невідома назва → сама назва (краще показати
 * технічне ніж нічого; ніколи не кидає — UI-хелпер).
 * @param {string} name
 * @returns {string}
 */
export function stageLabel(name) {
  return STAGE_LABELS[name] || String(name || 'Обробка');
}
