// ── normalizeCaseNoKey ───────────────────────────────────────────────────────
// Спільний хелпер дедуплікації справ ЄСІТС за номером справи (`case_no`).
// Один сенс (правило #11): "звести представлення case_no до стабільного
// ключа порівняння". НЕ змінює саме збережене значення case_no — повний
// рядок лишається для показу і денормалізованих контекстів. Ключ
// використовується ЛИШЕ для матчингу.
//
// Адвокат уточнив (діагностика 2026-05-27): номер справи виду
// `суд/порядковий/рік[-літера]` (напр. `363/2241/24`, `761/15469/20-Ц`)
// унікальний і постійний — усі провадження живуть під ним. Тому
// дедуплікація саме за `case_no`, а не за per-proceeding `ecitsCaseId`.
//
// Кроки нормалізації:
//   1. trim — оточуючі пробіли
//   2. внутрішні пробіли — прибрати
//   3. lower-case — щоб `-Ц` і `-ц` зливались
//   4. кінцевий суфікс-літера(и) `-[літери]$` — відкинути (для `-ц` запис
//      без суфікса і з суфіксом мають вважатись одним номером справи)
//
// НЕ нормалізує: розділювачі `/` (вони — обов'язкова структура), кирилиця/
// латиниця у суфіксі (lower-case це покриває). НЕ кидає винятків —
// для null/undefined/нерядка повертає `null` (caller вирішує що це значить).

/**
 * Нормалізує case_no у стабільний ключ для дедуплікації.
 *
 * @param {string|null|undefined} caseNo — повний рядок case_no.
 * @returns {string|null} нормалізований ключ або null якщо вхід не рядок/порожній.
 */
export function normalizeCaseNoKey(caseNo) {
  if (typeof caseNo !== 'string') return null;
  let key = caseNo.trim();
  if (key === '') return null;
  key = key.replace(/\s+/g, '');
  key = key.toLowerCase();
  // Відкидаємо кінцевий суфікс-літеру(и) типу `-ц`, `-к`, `-a` після
  // нормалізації до lower-case. ВАЖЛИВО: `\p{L}` ловить як кириличні
  // так і латинські літери (Unicode). Цифри не зачіпаємо — рік є частина
  // структури NNN/NNNNN/NN, не суфікс.
  key = key.replace(/-\p{L}+$/u, '');
  return key;
}
