// ── ECITS SAFETY ─────────────────────────────────────────────────────────────
// Безпекові межі для Court Sync сценаріїв що виконуються через зовнішнього
// агента (зараз Claude for Chrome, у майбутньому — власне розширення Legal BMS).
//
// Один сенс (правило #11): "категоричні заборони — які кнопки НЕ натискати,
// які дії НЕ виконувати". Це НЕ перевірки права (це — entitlements), НЕ
// валідація даних (це — schema/validateDocument), НЕ rate-limit. Це
// білий-список UI-категорій що адвокат довірив зовнішньому агенту і
// чорний-список — куди агент не сміє лізти навіть якщо «здається корисним».
//
// Чому це окремий файл а не частина promptBuilder: список може зростати з
// досвідом експлуатації (нова кнопка в кабінеті, новий тип посилання що
// веде поза cabinet.court.gov.ua). Зміна тут не повинна тригерити
// перегенерацію всього промпту, лише блоку safety.
//
// MVP TASK 0.4: експортується через buildSafetyBlock() і вшивається в
// промпт для Claude for Chrome. Майбутнє власне розширення читатиме ті ж
// списки програмно (детермінований enforcement у DOM-обгортці).

/**
 * Кнопки/посилання/елементи UI які агент НЕ ДОЛЖЕН натискати в кабінеті ЄСІТС.
 * Зростає за досвідом. Регістр оригінальний (як показує кабінет).
 */
export const ECITS_NEVER_TOUCH = Object.freeze([
  'НАДАТИ ЗГОДУ',
  'ОСКАРЖИТИ',
  'СТВОРИТИ ЗАЯВУ',
  'ВІДКЛАСТИ',
  'ВИДАЛИТИ',
  'ВИДАЛИТИ ВСЕ',
  'НАДІСЛАТИ',
  'ПІДТВЕРДИТИ',
  'СПЛАТИТИ',
  'ЗБЕРЕГТИ',
  'ЗМІНИТИ',
]);

/**
 * Категорії дій які заборонені незалежно від конкретної кнопки.
 * Це декларація «що Claude for Chrome НЕ робить навіть якщо здається корисним».
 */
export const ECITS_NEVER_DO = Object.freeze([
  'javascript_tool без явного дозволу',
  'створення Google Documents',
  'доступ до файлової системи поза браузером',
  'OAuth токени Drive API',
  'посилання за межі cabinet.court.gov.ua',
  'скачування файлів у MVP',
  'натискання посилань на папки з ключем КЕП',
]);

/**
 * Будує текстовий блок для вшивання у промпт Claude for Chrome.
 * Окремо ECITS_NEVER_TOUCH (UI-елементи) і ECITS_NEVER_DO (категорії).
 * Закінчується інструкцією «при блокуванні — доповідай, не шукай обхідні».
 *
 * @returns {string}
 */
export function buildSafetyBlock() {
  return [
    'КАТЕГОРИЧНО НЕ НАТИСКАЙ:',
    ...ECITS_NEVER_TOUCH.map((k) => `- ${k}`),
    '',
    'КАТЕГОРИЧНО НЕ РОБИ:',
    ...ECITS_NEVER_DO.map((d) => `- ${d}`),
    '',
    'ПРИ БЛОКУВАННІ: доповідай, не шукай обхідні шляхи.',
  ].join('\n');
}

/**
 * Перевіряє чи рядок (наприклад label кнопки) збігається з one of NEVER_TOUCH.
 * Регістронечутлива. Для майбутнього програмного enforcement у власному
 * розширенні: перехопити клік, звірити label, заблокувати.
 *
 * @param {string} label
 * @returns {boolean}
 */
export function isForbiddenAction(label) {
  if (typeof label !== 'string') return false;
  const upper = label.trim().toUpperCase();
  return ECITS_NEVER_TOUCH.some((forbidden) => upper === forbidden.toUpperCase());
}
